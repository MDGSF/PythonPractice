#!/usr/bin/env python
# -*- coding: UTF-8 -*-


# abs(x)
#   Return the absolute value of a number.
#   The argument may be an integer or a floating point number.
#   If the argument is a complex number, its magnitude is returned.


def main():
    print(abs(-1))
    print(abs(-1.1))
    print(abs(1))
    print(abs(1.1))


if __name__ == "__main__":
    main()
