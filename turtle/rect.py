#!/usr/bin/env python
# -*- coding: UTF-8 -*-

import turtle as t


def main():
    t.width(4)

    t.forward(200)
    t.right(90)

    t.pencolor('red')
    t.forward(100)
    t.right(90)

    t.pencolor('green')
    t.forward(200)
    t.right(90)

    t.pencolor('blue')
    t.forward(100)
    t.right(90)

    t.done()

if __name__ == "__main__":
    main()
